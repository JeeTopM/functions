"""
–ù–µ–æ–±—ã—á–Ω–∞—è —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ üå∂Ô∏è
–î–∞–Ω–∞ —Å—Ç—Ä–æ–∫–∞, —Å–æ–¥–µ—Ä–∂–∞—â–∞—è –ª–∞—Ç–∏–Ω—Å–∫–∏–µ –±—É–∫–≤—ã –∏ —Ü–∏—Ñ—Ä—ã. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è —Å–æ—Ä—Ç–∏—Ä—É–µ—Ç —Å–∏–º–≤–æ–ª—ã –≤ —Å—Ç—Ä–æ–∫–µ —Å–æ–≥–ª–∞—Å–Ω–æ —Å–ª–µ–¥—É—é—â–∏–º –ø—Ä–∞–≤–∏–ª–∞–º:
–≤—Å–µ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã —Å—Ç–æ—è—Ç –ø–µ—Ä–µ–¥ –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏ –±—É–∫–≤–∞–º–∏
–≤—Å–µ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∑–∞–≥–ª–∞–≤–Ω—ã–µ –±—É–∫–≤—ã —Å—Ç–æ—è—Ç –ø–µ—Ä–µ–¥ —Ü–∏—Ñ—Ä–∞–º–∏
–≤—Å–µ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –Ω–µ—á–µ—Ç–Ω—ã–µ —Ü–∏—Ñ—Ä—ã —Å—Ç–æ—è—Ç –ø–µ—Ä–µ–¥ –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–º–∏ —á–µ—Ç–Ω—ã–º–∏
"""
s = input().strip()

# –†–∞–∑–¥–µ–ª—è–µ–º —Å–∏–º–≤–æ–ª—ã –Ω–∞ –≥—Ä—É–ø–ø—ã –∏ —Å—Ä–∞–∑—É —Å–æ—Ä—Ç–∏—Ä—É–µ–º
lowercase = sorted([c for c in s if c.islower()])
uppercase = sorted([c for c in s if c.isupper()])
digits = sorted([c for c in s if c.isdigit()], key=lambda x: (int(x) % 2 == 0, x))

result = ''.join(lowercase + uppercase + digits)
print(result)


